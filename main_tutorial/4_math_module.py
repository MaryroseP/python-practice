import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) # returns the number to the nearest whole integer
print(math.ceil(pi)) # returns the number to nearest whole integer rounded up
print(math.floor(pi)) # returns the number to the nearest whole integer rounded down
print(abs(pi)) # returns the absolute value
print(pow(pi,2)) # this returns the number raised to a degree of power (which in this case is 2). The result is 9.8596.
print(math.sqrt(9.8596)) # returns the square root of a number
print(max(x,y,z)) # returns the maximum value
print(min(x,y,z)) # returns the minimum value
